public class Aritmetica {

  //Métodos de la clase
  Double sumar (double a, double b){ //El primero método de la clase Calculadora es sumar:
    return a + b;
  }

  Double restar (double a, double b){
    return a - b;
  }

  Double multiplicar (double a, double b){
    return a * b;
  }

  Double cuadrado (double a){
    return a*a;
  }

  Double raiz (double   a){
    return Math.sqrt(a);
  }
}
