/*
* Ejemplo de la clase abstracta.
* Se genera la clase abstracta SerVivo que contendrá el método abstracto Alimentarse.
* En esta clase no se instancian objetos ni el método, se instancian objetos en sus clases hijas.
*
* Esta clase (abstracta) solo sirve como super clase
*/

public abstract class SerVivo { //La clase debe ser abstracta

  public abstract void Alimentarse ();

}
