public class Operaciones {
  public static void main (String args []) {
    int a = 4;
    int b = 4;

    System.out.println("La operacion 4 + 4 / 2");

    int c = a + b / 2;
    System.out.println ("El resultado correcto es = " + c);

    int d = (a + b) / 2;
    System.out.println ("El resultado incorrecto es = " + d);
  }
}
