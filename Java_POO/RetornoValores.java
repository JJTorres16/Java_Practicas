public class RetornoValores {
  //Atributos:

  //Métodos con retorno de variables:
  //Método para sumar:
  public int sumar (int num1, int num2){
    int suma = num1 + num2;
    return suma;
  }

    //Método para restar:
    public int restar (int num1, int num2){
      int resta = num1-num2;
      return resta;
    }

    //Método para multiplicar:
    public int multiplicar (int num1, int num2){
      int mult = num1*num2;
      return mult;
    }

    //Método para dividir:
    public float dividir (int num1, int num2){
      float div = (float)num1 / (float)num2;
      return div;
  }
}
